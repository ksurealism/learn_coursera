from coursera_фибоначчи_порядок_числа import serial_num_fibonacci

assert serial_num_fibonacci(0) == 0, "При входном значении 0, должно выводиться 0"
assert serial_num_fibonacci(1) == 1, "При входном значении 1, должно выводиться 1"
assert serial_num_fibonacci(2) == 3, "При входном значении 2, должно выводиться 3"
assert serial_num_fibonacci(8) == 6, "При входном значении 8, должно выводиться 6"
assert serial_num_fibonacci(10) == -1, "При входном значении 10, должно выводиться -1"
assert serial_num_fibonacci(13) == 7, "При входном значении 13, должно выводиться 7"
